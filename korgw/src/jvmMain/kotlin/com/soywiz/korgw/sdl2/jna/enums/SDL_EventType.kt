    WINDOWEVENT(0x200),
    SYSWMEVENT(0x201),

    KEYDOWN(0x300),
    KEYUP(0x301),
    TEXTEDITING(0x302),
    TEXTINPUT(0x303),
    KEYMAPCHANGED(0x304),

    MOUSEMOTION(0x400),
    MOUSEBUTTONDOWN(0x401),
    MOUSEBUTTONUP(0x402),
    MOUSEWHEEL(0x403),

    JOYAXISMOTION(0x600),
